package Java_basic_pgm;

public class Kopryatla_pyramid {
	
	public static void main(String[] args) 
	{
		
		for(int i=1;i<=4;i++ )
		{
			
			for(int j=1;j<=i;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		//don weles programme ulate aani sulte kela aahe 
		for(int i=5;i>=1;i--)
		{
			
			for(int j=1;j<=i;j++)
			{
				System.out.print("*");
			}
			
			
			System.out.println();//next line print
	
			
		}
	}
	

}
